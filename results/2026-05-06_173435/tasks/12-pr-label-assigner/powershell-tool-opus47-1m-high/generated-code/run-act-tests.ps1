<#
.SYNOPSIS
  End-to-end harness: runs every test case through the GitHub Actions
  workflow under nektos/act and verifies exact label output.

.DESCRIPTION
  The workflow exposes three matrix cases that each invoke
  Get-PRLabels.ps1 against a fixture and assert the output JSON matches
  an expected literal. Running `act push --rm` once exercises all three
  cases plus the Pester job in a single act invocation, which keeps us
  inside the 3-act-push budget.

  After act exits, this harness:
    - saves combined stdout+stderr to act-result.txt (overwriting any
      prior file)
    - asserts act exit code 0
    - parses each `CASE_RESULT case=… observed=… expected=…` line and
      asserts `observed == expected` for the three cases
    - asserts every job ended with `Job succeeded`
    - validates the workflow structure (parsed text checks) and that
      actionlint passes
#>
[CmdletBinding()]
param()

$ErrorActionPreference = 'Stop'
Set-StrictMode -Version Latest

$ProjectRoot = $PSScriptRoot
$ResultFile  = Join-Path $ProjectRoot 'act-result.txt'

# ----------------- workflow structure tests (no act) -----------------

function Test-WorkflowStructure {
    Write-Host "::group::Workflow structure tests"
    $wfPath = Join-Path $script:ProjectRoot '.github/workflows/pr-label-assigner.yml'

    if (-not (Test-Path $wfPath)) { throw "Workflow file missing: $wfPath" }
    Write-Host "  [+] workflow file exists"

    $raw = Get-Content -Raw -Path $wfPath

    foreach ($trigger in @('push:', 'pull_request:', 'workflow_dispatch:')) {
        if ($raw -notmatch [regex]::Escape($trigger)) {
            throw "Workflow missing expected trigger: $trigger"
        }
    }
    Write-Host "  [+] triggers (push, pull_request, workflow_dispatch) present"

    foreach ($job in @('test:', 'assign-labels:')) {
        if ($raw -notmatch [regex]::Escape($job)) {
            throw "Workflow missing expected job: $job"
        }
    }
    Write-Host "  [+] jobs (test, assign-labels) present"

    if ($raw -notmatch 'Get-PRLabels\.ps1') {
        throw "Workflow does not reference Get-PRLabels.ps1"
    }
    if (-not (Test-Path (Join-Path $script:ProjectRoot 'Get-PRLabels.ps1'))) {
        throw "Get-PRLabels.ps1 referenced by workflow does not exist"
    }
    Write-Host "  [+] workflow references Get-PRLabels.ps1 (file exists)"

    foreach ($fx in @(
            'fixtures/case-multi-file-union.txt',
            'fixtures/case-empty.txt',
            'fixtures/case-priority.txt')) {
        if (-not (Test-Path (Join-Path $script:ProjectRoot $fx))) {
            throw "Fixture referenced by workflow missing on disk: $fx"
        }
    }
    Write-Host "  [+] all matrix fixtures exist"

    if ($raw -notmatch 'actions/checkout@v4') {
        throw "Workflow should use actions/checkout@v4"
    }
    Write-Host "  [+] uses actions/checkout@v4"

    if ($raw -notmatch 'shell:\s*pwsh') {
        throw "Workflow should use 'shell: pwsh' for run steps"
    }
    Write-Host "  [+] uses shell: pwsh"
    Write-Host "::endgroup::"
}

function Test-Actionlint {
    Write-Host "::group::actionlint validation"
    Push-Location $script:ProjectRoot
    try {
        $output = & actionlint .github/workflows/pr-label-assigner.yml 2>&1 | Out-String
        $code = $LASTEXITCODE
        Write-Host $output
        if ($code -ne 0) { throw "actionlint failed with exit $code" }
        Write-Host "  [+] actionlint exit 0"
    } finally {
        Pop-Location
    }
    Write-Host "::endgroup::"
}

# ----------------- act run -----------------

# Each (case, expectedJson) pair MUST match the matrix in the workflow.
$Cases = @(
    @{ Name = 'multi-file-union'; Expected = '["api","ci","documentation","source","tests"]' },
    @{ Name = 'empty-changeset';  Expected = '[]' },
    @{ Name = 'priority-mode';    Expected = '["api","documentation","source"]' }
)

function Invoke-AllCasesViaAct {
    Write-Host "::group::act push (single invocation, all matrix cases)"
    Push-Location $script:ProjectRoot
    try {
        Write-Host "  Running: act push --rm"
        $combined = & act push --rm 2>&1 | Out-String
        $exitCode = $LASTEXITCODE
    } finally {
        Pop-Location
    }
    Write-Host "::endgroup::"

    $header = @"
# act-result.txt — generated by run-act-tests.ps1
# act push exit code: $exitCode

==============================================================================
=== act push --rm (single invocation, all matrix cases)
==============================================================================
"@
    Set-Content -Path $script:ResultFile -Value $header
    Add-Content -Path $script:ResultFile -Value $combined

    if ($exitCode -ne 0) {
        throw "act exited with $exitCode (expected 0). See $script:ResultFile."
    }
    Write-Host "[+] act exit code 0"

    # Per-case label assertions. The workflow prints exactly one
    # `CASE_RESULT case=NAME observed=JSON expected=JSON` line per matrix
    # leg.
    $lines = $combined -split "`r?`n"
    foreach ($case in $script:Cases) {
        $name = $case.Name
        $expected = $case.Expected
        $hit = $lines | Where-Object { $_ -match "CASE_RESULT case=$([regex]::Escape($name))\b" } | Select-Object -First 1
        if (-not $hit) {
            throw "No CASE_RESULT line for case '$name'."
        }
        if ($hit -notmatch "observed=(\S+)\s+expected=(\S+)") {
            throw "Could not parse CASE_RESULT for '$name': $hit"
        }
        $observed = $Matches[1]
        $expFound = $Matches[2]
        if ($expFound -ne $expected) {
            throw "[$name] expected literal mismatch — workflow says '$expFound', harness expects '$expected'."
        }
        if ($observed -ne $expected) {
            throw "[$name] label mismatch — observed=$observed expected=$expected"
        }
        Write-Host "[+] case '$name' labels=$observed (matches expected)"
    }

    # Job-success assertions. The matrix expands to N legs of
    # 'assign-labels' plus the 'test' job, so we expect at least 4 lines.
    $succeededCount = ([regex]::Matches($combined, 'Job succeeded')).Count
    $expectedJobs = $script:Cases.Count + 1   # matrix legs + Pester job
    if ($succeededCount -lt $expectedJobs) {
        throw "Expected at least $expectedJobs 'Job succeeded' lines (Pester + $($script:Cases.Count) matrix legs); got $succeededCount."
    }
    Write-Host "[+] $succeededCount jobs reported 'Job succeeded' (>= $expectedJobs required)"
}

# ============================================================
# Run
# ============================================================

Test-WorkflowStructure
Test-Actionlint
Invoke-AllCasesViaAct

Write-Host ""
Write-Host "=========================================="
Write-Host "ALL TESTS PASSED."
Write-Host "Results saved to: $script:ResultFile"
Write-Host "=========================================="
